package Fundamentos

fun main(args: Array<String>) {
    // Tipos Numéricos Inteiros
    val num1: Byte = 127 // varia entre -128 e 127
    val num2: Short = 32767 // varia entre -32768 e 32767
    val num3: Int = 2_147_483_647 // varia entre -2147483648 e 2147483647
    val num4: Long = 9_223_372_036_854_775_807 // varia entre -9223372036854775808 e 9223372036854775807

    // Tipos Numéricos Reais
    val num5: Float = 3.14F // Float precisa do F no final varia entre 1.40239846E-45 e 3.4028235E38
    val num6: Double = 3.14 // varia entre 4.94065645841247E-324 e 1.7976931348623157E308

    // Tipo Caractere
    val char: Char = '?' // Outros exemplos... '1', 'g', ' '

    // Tipo Booleano
    val boolean: Boolean = true // ou false

    // Saída de Dados
    println(listOf(num1, num2, num3, num4, num5, num6, char, boolean)))

    // Tudo é objeto
    println(10.dec())
}
